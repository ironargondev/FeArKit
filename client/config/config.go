// RuntimeID is a random UUID generated once when the process starts.
// It is unique per running instance and stable across WebSocket reconnects,
// making it suitable as the device identity sent to the server.
var RuntimeID string

func init() {
	buf := make([]byte, 16)
	rand.Reader.Read(buf)
	RuntimeID = hex.EncodeToString(buf)
}

